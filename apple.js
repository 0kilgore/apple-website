function displayContent(content) {
    switch (content) {
      case 'home':
        window.location.href = 'apple.html';
        break;
      case 'Job':
        window.location.href = 'job.html';
        break;
      case 'about':
        window.location.href = 'about.html';
        break;
      case 'support':
        window.location.href = 'support.html';
        break;
      case 'accessories':
        window.location.href = 'accessories.html';
        break;
      case 'fix it':
        window.location.href = 'fix it.html';
        break;
      case 'mac':
        window.location.href = 'mac.html';
        break;
      default:
        break;
    }
  }


